package com.naposcsib;

public class TwoNumbers {
    public static void main(String[] args) {
        int i = 13;
        int j = 22;

        System.out.println(i + j);
        System.out.println(i - j);
        System.out.println(i * j);
        System.out.println(13 / 22);
        System.out.println(13. / 22.);
        System.out.println(i % j);

    }
}
